    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
         Words w = new Words();
        try {
           w.setMeaning(txt_meaning.getText().toString());
            w.setTransciption(txt_tranc.getText().toString());
             w.setTypeOfWord(txt_TOW.getText().toString());
              w.setWord(txt_word.getText().toString());
            if(new WordDAO().addNewWord(w))
            {
                JOptionPane.showMessageDialog(rootPane,"Add new word successful!");
                
            }
            else JOptionPane.showMessageDialog(rootPane,"Add new word failed");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_addActionPerformed

